public class Process
{
    private String processName;
    private int processSize;


    public void setName(String name)
    {
        processName=name;
    }
    public String getName()
    {
        return processName;
    }
    public void setSize(int s)
    {
        processSize=s;
    }
    public int getSize()
    {
        return processSize;
    }
}
